function add(x, y) {
	return (x + y);
}
function subtract(x, y) {
	return (x - y);
}
function divide(x, y) {
	return (x / y);
}
function multiply(x, y) {
	return (x * y);
}
function increment(x) {
	return (x + 1);
}
function decrement(x) {
	return (x - 1);
}
function makeInt(n, base10) {
	let a = parseInt(n, base10);
	return a;
}
function preserveDecimal(n) {
	return parseFloat(n);
}